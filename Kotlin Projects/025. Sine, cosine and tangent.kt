package com.example.myapplication

import kotlin.math.*

fun main(){
    val sin90 = sin(Math.PI/2)
    //sin90 is 1.0

    val cos180 = cos(Math.PI)
    //cos180 is -1.0

    val tan45 = tan(Math.PI/4)
    //tan45 is 0.9999999999

    println("sin90 is $sin90")
    println("cos180 is $cos180")
    println("tan45 is $tan45")
}