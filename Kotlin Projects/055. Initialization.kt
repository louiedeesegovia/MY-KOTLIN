package com.example.myapplication

fun main(){
    val number: Int = 42
    val nullInt: Int? = null
    val pi: Double? = 3.14
    val charA: Char? = 'A'
    val hasValue: Boolean? = true
    val noValue: Boolean? = null

    println("number is $number")
    println("nullInt is $nullInt")
    println("pi is $pi")
    println("charA is $charA")
    println("hasValue is $hasValue")
    println("noValue is $noValue")
}